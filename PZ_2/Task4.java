package com.company;

class Main {

    public static void main(String[] args) {

        int i = 35;
        String str = Integer.toString(i);
        System.out.println(str);

        double  i2 = 32.4e10;
        String str1 = Double.toString(i2);
        System.out.println(str1);

        long  i3 = 3422222;
        String str2 = Long.toString(i3);
        System.out.println(str2);

        float  i4 = 3.46f;
        String str3 = Float.toString(i4);
        System.out.println(str3);



    }

}
